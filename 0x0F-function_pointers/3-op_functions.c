#include "3-calc.h"

/**
 * op_add - adds tow intgers
 * @a: first int
 * @b: secont int
 * Return: result of adding
 */
int	op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - sub tow intgers
 * @a: first int
 * @b: secont int
 * Return: result of sub
 */
int	op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - mulitpilcate tow intgers
 * @a: first int
 * @b: secont int
 * Return: result of multiplication
 */
int	op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide first by second
 * @a: first int
 * @b: secont int
 * Return: result of division
 */
int	op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulate first by second
 * @a: first int
 * @b: secont int
 * Return: result of modulation
 */
int	op_mod(int a, int b)
{
	return (a % b);
}
